package org.anaadyanta.anaadyanta2k16.fragments.TechFragments;


import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import com.github.florent37.materialviewpager.MaterialViewPagerHelper;
import com.github.ksoichiro.android.observablescrollview.ObservableScrollView;

import org.anaadyanta.anaadyanta2k16.R;

/**
 * A simple {@link Fragment} subclass.
 */
public class LineFollowerFragment extends Fragment {

    ObservableScrollView lineFollowerScrollView;

    public static LineFollowerFragment getInstance() {
        return new LineFollowerFragment();
    }

    public LineFollowerFragment() {
        // Required empty public constructor
    }


    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view=inflater.inflate(R.layout.fragment_line_follower, container, false);
        return view;
    }

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        lineFollowerScrollView = (ObservableScrollView) view.findViewById(R.id.lineFollowerScrollView);
        MaterialViewPagerHelper.registerScrollView(getActivity(), lineFollowerScrollView, null);
    }

}
